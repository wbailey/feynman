package feynman.framework.system;

/**
 * Sub-class to define a <b>PhysicalObject</b> for the <b>CartesianSystem</b>.
 *
 * @author Wes Bailey
 * @version $Revision: 1.1.1.1 $ $Date: 2002/11/12 02:25:42 $
 */
abstract public class CartesianObject extends PhysicalObject {
	
	// Define the coordinate system that is the same for all Cartesian Objects.
	private double x;
	private double y;
	private double z;
	
	/**
	 * Method to define the X coordinate of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final void setX(double x) {
		this.x = x;
	}
	
	/**
	 * Use this method to retrieve the X coordinate value of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final double getX() {
		return x;
	}
	
	/**
	 * Method to define the Y coordinate of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final void setY(double y) {
		this.y = y;
	}
	
	/**
	 * Use this method to retrieve the Y coordinate value of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final double getY() {
		return y;
	}
	
	/**
	 * Method to define the Z coordinate of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final void setZ(double z) {
		this.z = z;
	}
	
	/**
	 * Use this method to retrieve the Z coordinate value of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final double getZ() {
		return z;
	}
	
}
