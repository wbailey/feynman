/**
 * This excpetion is generally invoked when the user has either configured the
 * properties file incorrectly by specifying not specifying a sub-class of the 
 * <b>PhysicalObject</b>that cannot be created by the Factory.
 *
 * @author Wes Bailey
 * @version $Revision: 1.1.1.1 $ $Date: 2002/11/12 02:25:43 $
 */
public class PhysicalObjectFactoryException extends Exception {
	
	// Only need package private contructors.
	public PhysicalObjectFactoryException() {
		super();
	}
	
	public PhysicalObjectFactoryException(String message) {
		super(message);
	}
	
}
