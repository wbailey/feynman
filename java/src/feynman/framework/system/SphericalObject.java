package feynman.framework.system;

/**
 * Sub-class to define a <b>PhysicalObject</b> for the <b>SphericalSystem</b>.
 *
 * @author Wes Bailey
 * @version $Revision: 1.1.1.1 $ $Date: 2002/11/12 02:25:42 $
 */
abstract public class SphericalObject extends PhysicalObject {
	
	// Define the coordinate system that is the same for all Cartesian Objects.
	private double r;
	private double theta;
	private double phi;
	
	/**
	 * Method to define the R coordinate of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final void setR(double r) {
		this.r = r;
	}
	
	/**
	 * Use this method to retrieve the X coordinate value of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final double getR() {
		return r;
	}
	
	/**
	 * Method to define the Theta coordinate of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final void setTheta(double theta) {
		this.theta = theta;
	}
	
	/**
	 * Use this method to retrieve the Theta coordinate value of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final double getTheta() {
		return theta;
	}
	
	/**
	 * Method to define the Phi coordinate of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final void setPhi(double phi) {
		this.phi = phi;
	}
	
	/**
	 * Use this method to retrieve the Phi coordinate value of the object.
	 * <br>
	 * <b>Note: </b>This method should <em>NOT</em> be overriden in the user implementation.
	 */
	public final double getPhi() {
		return phi;
	}
	
}
